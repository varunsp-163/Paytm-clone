const mongoose = require("mongoose");

const { MONGODB_CONNECTION } = require("./config");

mongoose.connect(MONGODB_CONNECTION);

// the schema for user

const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
    lowercase: true,
    minLength: 3,
    maxLength: 30,
  },
  password: {
    type: String,
    required: true,
    minLength: 6,
  },
  firstName: {
    type: String,
    required: true,
    trim: true,
    maxLength: 50,
  },
  lastName: {
    type: String,
    required: true,
    trim: true,
    maxLength: 50,
  },
});

const User = mongoose.model("User", userSchema);

// schema for user's account

const userAccount = new mongoose.Schema({
  userId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  balance: {
    type: Number,
    required: true,
  },
});

const UserAccount = mongoose.model("UserAccount", userAccount);

module.exports = {
  User,
  UserAccount,
};
